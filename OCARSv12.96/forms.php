<h4><u>Abstract from Police Records</u></h4>

<p>This form is issued by the Police whenever a person reports loss of property.</p>

<p>It is filled giving details of the lost property. The Officer Commanding Police Station(OCS), 
must sign and rubber-stamp the filled form and an official receipt issued.</p>

<p>NB. Once this form is filled,it must be taken to the nearest Police Station for necessary assistance.</p>


<h4><u>Medical Examination Report (P3)</u></h4>

<p>The Kenya Police Medical Examination form, popularly known as P3, is provided free of charge at our police stations. It is used to request 
for medical examination by a Medical Officer of Health, in order to determine the nature and extent of bodily injury sustained by a complainant(s) 
in assault cases.</p>

<p>Part I of the form must be filled by the Police Officer requesting medical examination.</p>

<p>Part II must befilled by a Medical Officer or Practitioner carrying out the examination giving medical details.</p>

<p>Section B of this form should be completed in all cases of assault, including sexual while section C is completed 
in cases alleged sexual offeces ony.</p>

<p>This form is a government document and must be returned to the police for use in adducing evidence in court.

Once the P3 form is filled in at the police station, the complainant is escorted by a police officer to a 
medical officer or practitioner for examination.</p>

<p>The form becomes an exhibit once produced in court.</p>
